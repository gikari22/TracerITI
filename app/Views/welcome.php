                            <p data-aos="fade-up" data-aos-delay="500"> Di Indonesia, pelaksanaan Tracer Study  umumnya masih terkendala dari sisi kebutuhan, sumber daya, dan metodologi dalam pelaksanaannya. Seringkali Tracer Study  dilakukan oleh perguruan tinggi hanya karena kebutuhan akreditasi, sehingga dilaksanakan secara tidak rutin. <br><br> Selain itu, sumber daya pelaksanaan Tracer Study  umumnya masih dianggap kurang memadai dan hal ini disertai dengan kesulitan dalam menerapkan metodologi yang tepat dalam pelaksanaannya.<br><br>    Pusat Pengembangan Karir  (CDC) ITI merupakan pusat karir di ITI yang diberi wewenang dan tanggungjawab dalam pelaksanaan Tracer Study . <br><br>Di tahun 2019 ini, CDC- ITI telah membentuk Tim Tracer Study  ITI 2019 dengan menggunakan metodologi yang telah disusun dengan baik, guna pengambilan data dari alumni secara tepat dan akurat.</p>
